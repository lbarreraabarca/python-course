## Basics operators

# Sum
x = 2 + 3
print(x)

# Substract
y = x - 4
print(y)

# Multiply
z = x * 8
print(z)

# Division
a = z / 13
round(a, 3)

# Advanced math functions.
import math

# Pow function receives two arguments.
b = math.pow(4,2)
# Square root function receives one argument.
c = math.sqrt(b)
# Cosine function receives one argument.
print(math.cos(0))
# Factorial function receives one argument.
print('factorial 5 : ', math.factorial(5))
